
public class Exemplo1 {

	public static void main(String[] args) {

		/*
		 * Operadores
		 * 
		 * <
		 * >
		 * <=
		 * >=
		 * ==
		 * !=
		 * 
		 * */

		int x = 5;
		
		if (x > 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		
		if (x < 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		
		if (x <= 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		
		if (x >= 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		
		if (x == 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
		
		if (x != 2) {
			System.out.println("true");
		}
		else {
			System.out.println("false");
		}
	}

}
