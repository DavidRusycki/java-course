
public class Exemplo4 {

	public static void main(String[] args) {

		/*
		 * Operadores de atribuição cumulativa
		 * 
		 * +=
		 * -+
		 * *=
		 * /=
		 * %=
		 * 
		 * */

		int a = 1;
		
		System.out.println(a += 1);
		
		a = 1;
		
		System.out.println(a -= 1);
		
		a = 2;
		
		System.out.println(a *= 2);
		
		a = 2;
		
		System.out.println(a /= 2);
		
		a = 2;
		
		System.out.println(a %= 2);
		
	}

}
